		<h5  class="Titulo">PERIODICO DIGITAL</h5>
		<h5 class="Titulo">PATRIA MASFERRERIANA</h5>
		<hr>
		<p id="p"> 
			La Facultad de Ciencias Empresariales “Licda. Daysi Carolina Marquina de Gómez” de la Universidad Salvadoreña Alberto Masferrer, en cumplimiento de su misión de “Formar profesionales con calidad humana” y tomando en cuenta proporcionar a sus estudiantes las oportunidades de su desarrollo profesional, plantea la iniciativa de desarrollar la idea de crear un medio de comunicación para práctica profesional, es así como  la iniciativa se convierte en proyecto el cual después de varias reuniones y con la aprobación de parte la Junta Central de Directores de la USAM,  toma la forma de un Periódico Digital, que se denomina “Patria Masferreriana”<br>“Patria Masferreriana” es un proyecto académico que proporciona a la universidad un medio de comunicación digital, bajo los valores institucionales y un aporte a la formación profesional de los estudiantes de Licenciatura en Comunicaciones y Técnico en Publicidad, en el cual podrán realizar actividades apegadas a la realidad de su carrera, en ejes periodísticos, relaciones públicas, imagen institucional, social y publicidad.
		</p>
		<hr>
		<h5 class="Titulo">Misión</h5>
		<p id="p">		
			Ser un medio de difusión objetivo y de promoción que destaque las actividades académicas, científicas, culturales y humanitarias de las diferentes facultades de la Universidad Salvadoreña Alberto Masferrer, mediante un ejercicio periodístico con alto sentido ético.
		</p>
		<h5 class="Titulo">Visión</h5>
		<p id="p">		
			Ser un medio de divulgación confiable cuyos contenidos reflejen calidad y responsabilidad ante la comunidad universitaria basado en los valores que rigen a la institución: verdad, justicia, moral, respeto, calidad humana, excelencia académica
		</p>
		<hr>

		<h5 class="Titulo">Objetivos:</h5>
		<p id="p">
			•	Contar con un medio en el que los estudiantes de la carrera de comunicaciones y publicidad puedan poner en práctica los conocimientos recibidos en su formación, bajo la dirección de profesionales con experiencia en la rama del ejercicio periodístico. <br>
			•	Difundir las actividades Institucionales y académicas, que la USAM realice. <br>
			•	Promover valores de respeto a la dignidad humana y medio ambiente, solidaridad, promoción de oportunidades de desarrollo en salud, emprendedurismo, cultura y arte. <br>
			•	Transmitir el pensamiento del Maestro Alberto Masferrer
		</p>
		<hr>
		<h5 class="Titulo">Ejes Temáticos:</h5>
		<p id="p">
			Estos son una especie de código de ética que guía el enfoque de los contenidos a divulgar. <br>
			•	Pensamiento del Maestro Alberto Masferrer <br>
			•	Respeto a los derechos humanos. <br>
			•	Equidad de género. <br>
			•	Desarrollo humano sostenible. <br> 
			•	Conservación de medio ambiente. <br>
			•	Migración <br>
			•	Desarrollo local. <br> 
			•	Identidad Nacional. <br>
			•	Vulnerabilidad.<br>
		</p>
